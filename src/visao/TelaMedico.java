    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        if(jTextFieldNome.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Preencha o Nome para Salvar!");
         jTextFieldNome.requestFocus();
        }else if(jTextFieldCrm.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Preencha o CRM para Salvar!");
        jTextFieldCrm.requestFocus();
        }else
        if(flag == 1){
        mod.setNome_medicos(jTextFieldNome.getText());
        mod.setEspecializacao((String)jComboBoxEspecialista.getSelectedItem());
        mod.setCrm(Integer.parseInt(jTextFieldCrm.getText()));
        contro.salvar(mod);
        jTextFieldNome.setText("");
        jTextFieldCrm.setText("");
        jTextFieldNome.setEnabled(false);
        jTextFieldCrm.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        preencherTabela("select * from medicos order by nome_medicos");
        }else{
        mod.setCodigo((Integer.parseInt(jcodID.getText())));
        mod.setNome_medicos(jTextFieldNome.getText());
        mod.setEspecializacao((String)jComboBoxEspecialista.getSelectedItem());
        mod.setCrm(Integer.parseInt(jTextFieldCrm.getText()));
        contro.editar(mod);
        jcodID.setText("");
        jTextFieldNome.setText("");
        jTextFieldCrm.setText("");
        jTextFieldNome.setEnabled(false);
        jTextFieldCrm.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonNovo.setEnabled(true);
        jButtonCancelar.setEnabled(false);
        preencherTabela("select * from medicos order by nome_medicos");
        
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

