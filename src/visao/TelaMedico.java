    private void jTableMedicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMedicosMouseClicked
        // TODO add your handling code here:
        String nome_medico =""+jTableMedicos.getValueAt(jTableMedicos.getSelectedRow(),1);
        con.conexao();
        con.executaSql("Select * from medicos where nome_medicos ='"+nome_medico+ "'");
       try {
           con.rs.first();
           jcodID.setText(String.valueOf(con.rs.getInt("cod_medico")));
           jTextFieldNome.setText(con.rs.getString("nome_medicos"));
           jComboBoxEspecialista.setSelectedItem(con.rs.getString("especialidade"));
           jTextFieldCrm.setText(con.rs.getString("crm"));
       } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Erro ao Selecionar dados n/" + ex);
           Logger.getLogger(TelaMedico.class.getName()).log(Level.SEVERE, null, ex);
       } 
        con.desconecta();
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
    }//GEN-LAST:event_jTableMedicosMouseClicked

