    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String []{"ID","Nome","Especialidade","Crm"};
        con.conexao();
        con.executaSql(Sql);
       try {
           con.rs.first();
           do{
            dados.add(new Object[]{con.rs.getInt("cod_medico"),con.rs.getString("nome_medicos"),con.rs.getString("especialidade"),con.rs.getInt("crm")});
           }while(con.rs.next());//enquanto estiver dados ele irar percorrer
       } catch (SQLException ex) {
         JOptionPane.showMessageDialog(null,"Busque um mÃ©dico que estÃ¡ contido na tabela");
           Logger.getLogger(TelaMedico.class.getName()).log(Level.SEVERE, null, ex);
       }
        ModeloTabela modelo = new ModeloTabela(dados,colunas);
        // o campo tabela estÃ¡ recebendo o parametro 'modelo'
        jTableMedicos.setModel(modelo);
        // passando a largura do campo
        jTableMedicos.getColumnModel().getColumn(0).setPreferredWidth(120);
        // o usuÃ¡rio nÃ£o podera redimensionar a tabela
        jTableMedicos.getColumnModel().getColumn(0).setResizable(false); 
        
        jTableMedicos.getColumnModel().getColumn(1).setPreferredWidth(200); 
        jTableMedicos.getColumnModel().getColumn(1).setResizable(false);  
        
        jTableMedicos.getColumnModel().getColumn(2).setPreferredWidth(200); 
        jTableMedicos.getColumnModel().getColumn(2).setResizable(false); 
        
        jTableMedicos.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTableMedicos.getColumnModel().getColumn(3).setResizable(false); 
       
        jTableMedicos.getTableHeader().setReorderingAllowed(false);
        jTableMedicos.setAutoResizeMode(jTableMedicos.AUTO_RESIZE_OFF);
        jTableMedicos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//Selecionar um item por vez
        
        
        con.desconecta();
    }
    
    
